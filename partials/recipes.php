<h1>Chicken Verde</h1>
<img src="img/recipes/chicken.png" align="right" class="recipe-photo" />
<p>
    Place 4-5 pieces chicken legs & thighs or breasts in slow cooker Cover with one 10 oz can green enchilada sauce.  Cook for 6 to 8 hours.
</p><ul>
    <li>30 minutes before serving, remove bones from meat, saute in olive oil and add to chicken</li>
    <li>1/2 medium chopped onion</li>
    <li>2 or 3 chopped garlic cloves</li>
    <li>1/2 tsp oregano</li>
    <li>2 tsp ground cumin</li>
</ul><p>
    If more sauce is desired, add one can cream of mushroom soup.<br />
    Roll in flour tortillas with grated cheese salsa and sour cream for Burritos
</p><h1>OR</h1><p>
    Layer with grated cheese and crushed corn chips for Enchilada Casserole bake at 350F for 25-30 minutes microwave for 15-20 minutes
</p><h1>OR</h1><p>Roll up in corn tortillas with grated cheese for Enchiladas top with sauce (an additional can of green enchilada sauce and cheese, bake at 350F for 30 minutes, or microwave for 15-20 minutes
</p><p>
    Top any of the above with your favorite toppings; salsa, fresh cilantro, lemon/lime wedges, hot sauce, salsa, shredded lettuce, diced tomato, and/or minced jalapeno.
</p>

