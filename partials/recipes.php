<h1>Bacon Grilled Asparagus</h1>
<img src="img/recipes/bacon-wrapped.png" align="right" class="recipe-photo" />
<h2>Ingredients</h2>
<ul>
    <li>1lb of asparagus</li>
    <li>1 lb. of bacon approx (depending on the thickness of the cut of bacon)</li>
</ul>

<p>
    Snap bottoms of asparagus off & wash.<br />
    Wrap 3-4 pieces of asparagus with 1 Strip of bacon. <br />
    Grill at 350 for about 8 minutes until bacon is cooked. <br />
    Indirect heat, or upper shelf on grill works best.<br />
</p>

